public class P1Q4 {
    public static void main(String[] args) {
        //Autoboxing
        int x=100;
        Integer X1=x;
        System.out.println("x = " + x);
        System.out.println("X1 = " + X1);

        //Unboxing
        Double d=100.0;
        double D1=d;
        System.out.println("d = " + d);
        System.out.println("D1 = " + D1);
    }
}
